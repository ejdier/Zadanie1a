package models;

public class CalculateRowModel {

    private int rate_number;
    private int rate_amount;
    private int rate_interest;
    private int rate_sum;

    public int getRate_number() {
        return rate_number;
    }

    public void setRate_number(int rate_number) {
        this.rate_number = rate_number;
    }

    public int getRate_amount() {
        return rate_amount;
    }

    public void setRate_amount(int rate_amount) {
        this.rate_amount = rate_amount;
    }

    public int getRate_interest() {
        return rate_interest;
    }

    public void setRate_interest(int rate_interest) {
        this.rate_interest = rate_interest;
    }

    public int getRate_sum() {
        return rate_sum;
    }

    public void setRate_sum(int rate_sum) {
        this.rate_sum = rate_sum;
    }
}
